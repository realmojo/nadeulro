const FONT = "Apple SD Gothic Neo, AppleGothic, sans-serif";
const esc = (s) => s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");

// 제목 줄바꿈: 공백 우선 그리디 패킹(한글=1, 공백=0.5, 그 외=0.55)
function wrapTitle(title, maxUnits) {
  const width = (s) => [...s].reduce((a, c) => a + (c === " " ? 0.5 : /[가-힣]/.test(c) ? 1 : 0.55), 0);
  const words = title.split(" ");
  const lines = [];
  let cur = "";
  for (const w of words) {
    const cand = cur ? cur + " " + w : w;
    if (width(cand) <= maxUnits || !cur) cur = cand;
    else { lines.push(cur); cur = w; }
  }
  if (cur) lines.push(cur);
  return lines;
}

function coverSvg(title, cat) {
  const c = CAT[cat];
  const lines = wrapTitle(title, 13);
  const fs2 = lines.length >= 3 ? 62 : 72;
  const lh = fs2 + 18;
  const blockH = lines.length * lh;
  const startY = 315 - blockH / 2 + fs2 * 0.78; // 세로 중앙 정렬
  const titleSvg = lines
    .map((ln, i) => `<text x="80" y="${Math.round(startY + i * lh)}" font-family="${FONT}" font-size="${fs2}" font-weight="800" fill="#ffffff">${esc(ln)}</text>`)
    .join("");
  const contour = Array.from({ length: 8 }, (_, i) =>
    `<ellipse cx="1010" cy="120" rx="${110 + i * 92}" ry="${84 + i * 72}"/>`).join("");
  return `<svg xmlns="http://www.w3.org/2000/svg" width="1200" height="630">
  <defs><linearGradient id="g" x1="0" y1="0" x2="1" y2="1">
    <stop offset="0" stop-color="${c.color}"/><stop offset="1" stop-color="${c.deep}"/></linearGradient></defs>
  <rect width="1200" height="630" fill="url(#g)"/>
  <g fill="none" stroke="#ffffff" stroke-opacity="0.09" stroke-width="2">${contour}</g>
  <g transform="translate(80,72) scale(1.7)"><path d="${c.glyph}" fill="#ffffff" fill-opacity="0.92"/></g>
  <text x="130" y="100" font-family="${FONT}" font-size="30" font-weight="700" fill="#ffffff" fill-opacity="0.9">나들로 · ${esc(c.label)}</text>
  ${titleSvg}
  <rect x="80" y="540" width="46" height="4" rx="2" fill="#ffffff" fill-opacity="0.65"/>
  <text x="80" y="580" font-family="${FONT}" font-size="26" fill="#ffffff" fill-opacity="0.82">nadeulro.com</text>
</svg>`;
}

async function makeAndUpload(post) {
  const jpg = await sharp(Buffer.from(coverSvg(post.title, post.category))).jpeg({ quality: 88 }).toBuffer();
  const key = `blog/${post.category}/${post.slug}.jpg`;
  await s3.send(new PutObjectCommand({
    Bucket: BUCKET, Key: key, Body: jpg,
    ContentType: "image/jpeg", CacheControl: "public, max-age=31536000",
  }));
  return `https://${BUCKET}.s3.${REGION}.amazonaws.com/${key}?v=1`;
}

