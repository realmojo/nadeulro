function readEnv(path) {
  return Object.fromEntries(
    fs.readFileSync(path, "utf8").split("\n").filter((l) => l.includes("=")).map((l) => {
      const i = l.indexOf("=");
      return [l.slice(0, i).trim(), l.slice(i + 1).trim().replace(/^["']+|["']+$/g, "")];
    })
  );
}
